public class DemoMath
{
    public static void main(String[] args)
    {
        int a = -4;
        int b = 8;
        double c = 3.5;

        System.out.println("Value of  int a: " + a);
        System.out.println("Absolute value of int a: " + Math.abs(a));

        System.out.println("Value of int a: " + a);
        System.out.println("Value of int b: " + b);
        System.out.println("Max value of int a and int b: " + Math.max(a,b));

        System.out.println("Value of double c: " + c);
        System.out.println("Double c to the power of c: " + Math.pow(c,c));

        System.out.println("Value of pi: " + Math.PI);
    }
}
