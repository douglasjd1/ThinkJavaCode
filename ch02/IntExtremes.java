public class IntExtremes
{
    public static void main(String[] args)
    {
        int positiveInt =  2147483647;
        System.out.println(positiveInt);

        positiveInt++;

        System.out.println(positiveInt);

        int negativeInt = -2147483648;
        System.out.println(negativeInt);

        negativeInt--;

        System.out.println(negativeInt);

    }
}
