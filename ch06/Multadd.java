public class Multadd
{
    public static void main(String[] args)
    {
        double a = 1.0;
        double b = 2.0;
        double c = 3.0;

        System.out.println(multadd(a,b,c));

    }

    private static double multadd(double a, double b, double c)
    {
        return a * b + c;
    }
}
